#include <unistd.h>

void	ft_putchar(char c);
void	ft_putstr(char *str);

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		ft_putchar(*str);
		str++;
	}
}

int		main(int argc, char **argv)
{
	int arg_count;

	arg_count = argc - 1;
	while (arg_count != 0)
	{
		ft_putstr(argv[arg_count]);
		ft_putchar('\n');
		arg_count--;
	}
}
